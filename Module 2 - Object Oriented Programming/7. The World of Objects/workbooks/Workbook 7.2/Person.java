public class Person {
    // new object
    String name;
    String nationality;
    String dateOfBirth;
    int seatNumber;
//constructor
    public Person (String name, String nationality, String dateOfBirth, int seatNumber){
        this.name = name;
        this.nationality = nationality;
        this.dateOfBirth = dateOfBirth;
        this.seatNumber = seatNumber;
    }
 
}
